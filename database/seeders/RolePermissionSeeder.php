class RolePermissionSeeder extends Seeder
{
    /**
     * Run the database seeds.
     *
     * @return void
     */
    public function run()
    {
        $superAdmin = Role::where('slug','super-admin')->first();
        $permissions = Permission::get();
        $superAdmin->permissions()->attach($permissions);
    }
}
